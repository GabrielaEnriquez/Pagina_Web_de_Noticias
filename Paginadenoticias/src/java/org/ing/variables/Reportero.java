package org.ing.variables;

/**
 *
 * @author Israel
 */
public class Reportero {
    String nombreRepor;
    String usuarioReportero;
    String contraseñaReportero;

    public String getNombreRepor() {
        return nombreRepor;
    }

    public void setNombreRepor(String nombreRepor) {
        this.nombreRepor = nombreRepor;
    }

    public String getUsuarioReportero() {
        return usuarioReportero;
    }

    public void setUsuarioReportero(String usuarioReportero) {
        this.usuarioReportero = usuarioReportero;
    }

    public String getContraseñaReportero() {
        return contraseñaReportero;
    }

    public void setContraseñaReportero(String contraseñaReportero) {
        this.contraseñaReportero = contraseñaReportero;
    }
    
}
